

#Going through basic operators in Python3

print(1+2)  #Addition

print(2-1)  #Substraction

print(2*1)  #Multiplication

print(2/1)  #Division: PERFORMS TRUE DIVISION BY DEFAULT (WILL RETURN A DECIMAL NUMBER EVERYTIME)

print(2%1)  #Modulus

#A special feature of Python3 is that you can compute the power of a number using the '**' operator

print(2**2) #Will display 4
